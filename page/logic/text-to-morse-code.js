function textToMorseCode(text){
	var output = '';
	for (var i = 0; i < text.length; i++) {
		if (i !== 0) output += ' '; // word separator

		var tmp = morseCode[text.charAt(i).toLowerCase()];
		if (typeof tmp === 'undefined') {
			tmp = text.charAt(i);
		}
		output += tmp;
	}

	return output;
}